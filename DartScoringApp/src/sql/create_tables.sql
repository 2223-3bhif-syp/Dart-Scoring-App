create table Player
(
    ID     INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY,
    NAME   VARCHAR,
    POINTS INTEGER
);

create table Game
(
    ID             INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY,
    PLAYER_AMOUNT  INTEGER,
    CURRENT_PLAYER INTEGER,
    GAME_TYPE      INTEGER,
    CONSTRAINT     CURRENT_PLAYER REFERENCES Player (ID),
    CONSTRAINT     GAME_TYPE REFERENCES Gametype (ID)
);
create table Gametype
(
    ID        INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY,
    GAME_TYPE VARCHAR
)